#include "nm_utils.h"

int	nm_comp(char *s1, char *s2)
{
	unsigned char	a;
	unsigned char	b;

	a = 0;
	b = 0;
	while (*s1 || *s2)
	{
		if (*s1 != *s2)
			break ;
		++s1;
		++s2;
	}
	a = *s1;
	b = *s2;
	if (*s1 == '@')
		a = 255;
	if (*s2 == '@')
		b = 255;
	return (a - b);	
}
