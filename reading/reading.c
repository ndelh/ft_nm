#include "../ft_nm.h"

void	read_map(t_nm *nm)
{
	char	*s;
	void	*end;

	end = nm->map_end + 1;
	s = (char *)nm->map_begin;

	while (s != end)
	{
		write(1, s, 1);
		++s;
	}
	write(1, "\n", 1);
}

